def main():
  #====== TUPLE to LIST integer example =======================
    number_tuple = (1, 2, 3)
    number_list = list(number_tuple)
    print(number_list)

  #====== TUPLE to LIST string example ========================
    str_list = ['one', 'two', 'three']
    str_tuple = tuple(str_list)
    print(str_tuple)

# Call the main function
if __name__ == '__main__':
    main()

