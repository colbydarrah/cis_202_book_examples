# demonstrates how to identify lowest and highest values in list.

def main():
    # Create a list.
    list_numbers = [1, 2, 3, 4, 5]

    # Display element/item with lowest value in sequence/list
    print('The lowest value is', min(list_numbers))

    # Display element/item with highest value in sequence/list
    print('The highest value is:', max(list_numbers))

# Call the main function
if __name__ == '__main__':
    main()